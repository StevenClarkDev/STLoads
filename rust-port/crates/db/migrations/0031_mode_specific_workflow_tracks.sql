ALTER TABLE loads
    ADD COLUMN IF NOT EXISTS mode_validation_status VARCHAR(32) NOT NULL DEFAULT 'validated',
    ADD COLUMN IF NOT EXISTS mode_validation_notes TEXT[] NOT NULL DEFAULT ARRAY[]::TEXT[];

CREATE TABLE IF NOT EXISTS load_mode_validation_events (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    load_id BIGINT NOT NULL REFERENCES loads(id) ON DELETE CASCADE,
    mode_key VARCHAR(64) NOT NULL,
    validation_status VARCHAR(32) NOT NULL,
    validation_notes TEXT[] NOT NULL DEFAULT ARRAY[]::TEXT[],
    actor_user_id BIGINT NULL REFERENCES users(id) ON DELETE SET NULL,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX IF NOT EXISTS idx_load_mode_validation_events_load
    ON load_mode_validation_events (load_id, created_at DESC);
