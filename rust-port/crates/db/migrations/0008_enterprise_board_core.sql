CREATE INDEX IF NOT EXISTS idx_stloads_posting_stops_window ON stloads_posting_stops (tenant_id, window_start, window_end);

CREATE TABLE IF NOT EXISTS stloads_posting_rates (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tenant_id VARCHAR(100) NOT NULL,
