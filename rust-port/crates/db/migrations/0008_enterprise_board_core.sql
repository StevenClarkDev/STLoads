    min_amount DECIMAL(12,2) NULL,
    max_amount DECIMAL(12,2) NULL,
    accessorial_policy JSONB NOT NULL DEFAULT '{}'::jsonb,
    is_book_now BOOLEAN NOT NULL DEFAULT FALSE,
    effective_at TIMESTAMP(6) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    expires_at TIMESTAMP(6) NULL,
    created_at TIMESTAMP(6) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP(6) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_stloads_posting_rates_tenant_id FOREIGN KEY (tenant_id) REFERENCES tenants(id) ON DELETE CASCADE,
