CREATE INDEX IF NOT EXISTS idx_stloads_posting_rates_posting ON stloads_posting_rates (tenant_id, posting_id, effective_at DESC);
CREATE INDEX IF NOT EXISTS idx_stloads_posting_rates_book_now ON stloads_posting_rates (tenant_id, is_book_now, amount, expires_at);

